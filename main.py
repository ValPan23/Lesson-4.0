
# Определяем переменные a и b
a = 5
b = 3
# Функция calculate_sum, вычисляющая сумму
def calculate_sum(a, b):
    return a + b

# передаём аргументы функции и сохраняем результат в переменную
result_sum = calculate_sum(a, b)

# Выводим результат
print('Сумма:', result_sum)

# Функция calculate_product, вычисляющая произведение
def calculate_product(a, b):
    return a * b

# передаём аргументы функции и сохраняем результат в переменную
result_product = calculate_product(a, b)

# Выводим результат
print('Произведение:', result_product)

